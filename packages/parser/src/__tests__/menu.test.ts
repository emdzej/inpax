import { describe, it, expect } from "vitest";
import { parseMenu } from "../menu.js";

describe("parseMenu", () => {
  it("should correctly parse menu with no items", () => {
    const data = new Uint8Array([
      0x02, 0x6D, 0x31, 0x0A, 0x00, 0x00, 0x00, 0x00, 0x0A, 0x0A, 0x00, 0x02, 0x00, 0x0F, 0x00, 0x00,
      0x00, 0x0C, 0x80, 0x04, 0x00,
    ]);

    const result = parseMenu(data, 0);
    expect(result.result.type).toBe(0x02);
    expect(result.result.name).toBe("m1");
    expect(result.result.id).toBe(0);
    expect(result.result.flags).toBe(0);
    expect(result.result.arg1).toBeUndefined();
    expect(result.result.arg2).toBeUndefined();
    expect(result.result.size).toBe(2);
    expect(result.result.instructions).toEqual([
      {
        raw: new Uint8Array([0x0F, 0x00, 0x00, 0x00]),
        offset: 13,
      }, {
        raw: new Uint8Array([0x0C, 0x80, 0x04, 0x00]),
        offset: 17,
      },
    ]);
    expect(result.result.items).toEqual([]);
  });

  it("should correctly parse menu with 1 item", () => {
    const data = new Uint8Array([
      0x02, 0x6D, 0x32, 0x0A, 0x00, 0x00, 0x00, 0x00, 0x0A, 0x0A, 0x00, 0x02, 0x00, 0x0F, 0x00, 0x00,
      0x00, 0x0C, 0x80, 0x04, 0x00, 0x24, 0x0A, 0x00, 0x00, 0x01, 0x00, 0x46, 0x31, 0x20, 0x4C, 0x61,
      0x62, 0x65, 0x6C, 0x0A, 0x0A, 0x00, 0x02, 0x00, 0x0F, 0x00, 0x00, 0x00, 0x0C, 0x80, 0x04, 0x00,
    ]);

    const result = parseMenu(data, 0);
    expect(result.result.type).toBe(0x02);
    expect(result.result.name).toBe("m2");
    expect(result.result.id).toBe(0);
    expect(result.result.flags).toBe(0);
    expect(result.result.arg1).toBeUndefined();
    expect(result.result.arg2).toBeUndefined();
    expect(result.result.size).toBe(2);

    expect(result.result.items.length).toBe(1);

    const item = result.result.items[0];
    expect(item.type).toBe(0x24);
    expect(item.name).toBeUndefined();
    expect(item.id).toBe(0);
    expect(item.flags).toBe(1);
    expect(item.arg1).toBe("F1 Label");
    expect(item.arg2).toBeUndefined();
    expect(item.size).toBe(2);
    expect(item.instructions).toEqual([
      {
        raw: new Uint8Array([0x0F, 0x00, 0x00, 0x00]),
        offset: 40,
      }, {
        raw: new Uint8Array([0x0C, 0x80, 0x04, 0x00]),
        offset: 44,
      },
    ]);

  });

   it("should correctly parse menu with 2 items", () => {
    const data = new Uint8Array([
      0x02, 0x6D, 0x5F, 0x6D, 0x61, 0x69, 0x6E, 0x0A, 0x02, 0x00, 0x00, 0x00, 0x0A, 0x0A, 0x00, 0x02, 
      0x00, 0x0F, 0x00, 0x00, 0x00, 0x0C, 0x80, 0x04, 0x00, 0x24, 0x0A, 0x00, 0x00, 0x01, 0x00, 0x46, 
      0x31, 0x20, 0x4C, 0x61, 0x62, 0x65, 0x6C, 0x0A, 0x0A, 0x00, 0x02, 0x00, 0x0F, 0x00, 0x00, 0x00, 
      0x0C, 0x80, 0x04, 0x00, 0x24, 0x0A, 0x00, 0x00, 0x02, 0x00, 0x46, 0x32, 0x20, 0x4C, 0x61, 0x62, 
      0x65, 0x6C, 0x0A, 0x0A, 0x00, 0x02, 0x00, 0x0F, 0x00, 0x00, 0x00, 0x0C, 0x80, 0x04, 0x00, 
  ]);

    const result = parseMenu(data, 0);
    expect(result.result.type).toBe(0x02);
    expect(result.result.name).toBe("m_main");
    expect(result.result.id).toBe(2);
    expect(result.result.flags).toBe(0);
    expect(result.result.arg1).toBeUndefined();
    expect(result.result.arg2).toBeUndefined();
    expect(result.result.size).toBe(2);

    expect(result.result.items.length).toBe(2);

  });
});
